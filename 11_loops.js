// Loops in JavaScript: for, while, do...while

// 1. For Loop
console.log("For Loop:");
for (let i = 1; i <= 5; i++) {
    console.log("Iteration number:", i);
}

// 2. While Loop
console.log("\nWhile Loop:");
let j = 1;
while (j <= 5) {
    console.log("Iteration number:", j);
    j++;
}

// 3. Do...While Loop
console.log("\nDo...While Loop:");
let k = 1;
do {
    console.log("Iteration number:", k);
    k++;
} while (k <= 5);